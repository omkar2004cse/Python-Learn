# Write a program to check whether a character is a vowel or consonant
ch=input("Enter a Character:-")
if ch=='a' or ch=='e' or ch=='i' or ch=='o' or ch=='u':
    print(ch,"is Vowel")
elif ch=='A' or ch=='E' or ch=='I' or ch=='O' or ch=="U":
    print(ch,"is Vowel")
else:
    print(ch,"is Consonant")
