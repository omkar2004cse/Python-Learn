# indexing and slicing
s=input("Enter a String:-")
i=int(input("Enter a index number:-"))
print('the letter is present at index',i,' is:-', s[i])

print("Slicing of string from index 2 to 7 are:-",s[2:7])

print("using slicing step:-",s[1:8:2])

# negative slicing
print("By Negative index:-",s[::-1])

print("Negative index and slicing:-",s[::-1])